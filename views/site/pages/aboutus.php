<h2>境象生物（AcebioX中国）</h2>

<h3>公司简介</h3>

<p>境象生物(AcebioX中国)是美国AcebioX团队在中国上海成立的公司，公司专业从事基因诊断试剂的研发、生产和销售，以及基因分析服务，是基因检测产品和自主研发PCR芯片的领军企业。境象生物是上海市重点支持企业，入驻奉贤区金斗路399号金斗产业园，已建有两栋独立超过2000平米的GMP生产，研发与办公大楼。</p>

<p>境象生物(AcebioX中国)是国家高新技术企业、领军型海归人才创办企业、省级创新创业双创人才创业企业、龙城英才重点创业企业，同时也是美国Agena公司官方认证中国区MassArray基因分型服务商。公司研发的PCR芯片被评为高新技术产品。美国AcebioX坐落于美国第二大城市，有“天使之城”之称的加州洛杉矶（ Los Angeles ），专注于基因分析产品的研发、生产和销售。</p>


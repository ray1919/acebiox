<h3>公司名称含意</h3>

<p>AcebioX, Ace是扑克牌中的A，意为顶级和卓越，bio是生物技术，X是数学中的未知数，意为任何技术和知识，也意为无限的想象空间。AcebioX意为顶级和卓越的生物技术公司，融合多元化的技术和知识，拥有无限的想象空间。</p>

<p>境象生物，世间万物，象由境生。对于生物而言，生命为象，基因为境。境象生物致力于成为全球生物技术的领军企业，通过分析生物的境-基因，优化生物的象-生命，从而让人人享有更健康更幸福的生活。</p>

<h3>创始人简介</h3>

<p>境象生物(AcebioX)创始人CEO雷向东博士，是上海市“千人计划”科学家，中科院生物化学和细胞生物学研究所博士，曾先后在美国路易斯维尔大学（UL），美国国立卫生研究院（NIH）和加州大学洛杉矶分校（UCLA）进行博士后研究工作，并先后在美国Mannkind公司，美国SABiosicences公司，以及德国Qiagen公司担任研发总监。</p>

<h3>主营业务</h3>

<p>境象生物(AcebioX)是一家集基因分析试剂研发，生产，销售以及基因分析服务为一体的综合性跨国生物技术企业，致力于研发，推广和应用基因分析技术，从而让专业的人员可以干更专业的事，努力提升社会健康水平和幸福指数。</p>

<h4>为大众人群</h4>

<p>提供个人疾病相关基因信息检测和分析服务，基于世界前沿权威科研成果，提供个体化用药及饮食营养和生活建议等。</p>

<h4>为科研人员</h4>

<p>提供多样化基因分析试剂和定制化的基因分析服务，例如，定量PCR, PCR芯片，SNP分型，一代测序，二代靶向测序科研试剂和服务等。</p>

<h4>为临床医生</h4>

<p>提供GMP规范的基因分析试剂以及临床基因检测服务和科研服务，例如肿瘤，妇幼，感染类检测试剂盒，临床和科研基因检测服务等。</p>


<p>PCR Array，PCR芯片，是一种高通量定量PCR方法，可以同时检测几十个至数百个目标基因的表达。</p>

<p>标准规范的qPCR实验包含引物设计合成，引物及实验体系验证，目标产物验证，上机及数据分析等多个步骤，详情请参考<a href="#">qPCR实验标准规范</a>。</p>

<p>通常从确定目标基因，设计或查找引物到最终获得理想的实验结果需要花费研究人员几个星期，如果引物验证不成功，需要重新设计验证，这将耗费更长时间。若要检测大量基因，则工作量巨大，耗时更长久。</p>

<h2>InstantPCRarray™，高通量qPCR，加样就上机</h2>

<p>InstantPCRarray™，高通量基因表达定量PCR芯片，采用SYBR® Green I实时荧光PCR进行基因表达定量，包含96孔和384孔两种规格，经过验证的基因专一性引物均已经固定在PCR芯片的孔中，加入样本就可上机实验，同时检测88个至数百个基因的表达情况。</p>

